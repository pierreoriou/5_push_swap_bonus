#include "libft.h"

void	pf_putnbr(long n)
{
	long long	nb;

	nb = n;
	if (nb < 0)
	{
		write(1, "-", 1);
		nb = -nb;
	}
	if (nb >= 10)
		pf_putnbr(nb / 10);
	pf_putchar(nb % 10 + '0');
}
